def generate_random_name():
    # 随机选择一个姓氏
    surname = random.choice(surnames)
    # 随机选择名字的长度（1 或 2 个字）
    name_length = random.choice([1, 2])
    name = ""
    if name_length == 1:
        # 随机选择一个名字字符
        name = random.choice(names)
    else:
        # 随机选择两个名字字符
        name = random.choice(names) + random.choice(names)
    # 组合姓氏和名字
    full_name = surname + name
    return full_name


